=head2 Stella!!!!!

Stella is named for Stella Adler who was an American actress turned teacher who
taught "Method Acting" to the likes of Marlon Brando, Steve McQueen, Robert DeNiro
and more.

L<https://en.wikipedia.org/wiki/Stella_Adler>

"Stella!!!!!" is also the line from the famous scence in "A Streetcar Named Desire"
which starred Marlon Brando.

L<https://www.youtube.com/watch?v=kYA9hvcLekg>

Lastly, a subtle little nod to David H. Adler (dha), who shares the same last
name as Stella, and whose love of (bad) movies was legandary in the Perl community.

L<https://metacpan.org/author/DHA>

